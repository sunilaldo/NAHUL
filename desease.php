    <section id="croup">
        <h2>Croup</h2>
        <p>Croup is a viral infection that causes swelling and inflammation of the airways, making it difficult for babies to breathe. It often results in a harsh, barking cough, and may also cause a hoarse voice and difficulty breathing.</p>
        <p>Treatment typically involves providing humidified air, keeping the baby calm, and in severe cases, medications such as corticosteroids may be prescribed.</p>
    </section>
    <section id="colic">
        <h2>Colic</h2>
        <p>Colic is a condition characterized by uncontrollable crying in an otherwise healthy baby. It usually starts within the first few weeks of life and can last for several months. The cause of colic is not fully understood, but it may be related to gastrointestinal discomfort or sensitivity to stimuli.</p>
        <p>There is no specific treatment for colic, but strategies such as comforting the baby, providing gentle motion, and ensuring they are well-fed and burped may help alleviate symptoms.</p>
    </section>
    <section id="RSV">
        <h2>Respiratory Syncytial Virus (RSV)</h2>
        <p>RSV is a common respiratory virus that can cause mild, cold-like symptoms in older children and adults, but it can be more serious in babies, especially those under 6 months old. Symptoms of RSV in babies may include coughing, wheezing, difficulty breathing, and fever.</p>
        <p>There is no specific treatment for RSV, but supportive care such as ensuring the baby gets plenty of fluids, using a cool mist humidifier, and suctioning mucus from the nose can help relieve symptoms. In severe cases, hospitalization may be necessary.</p>
    </section>
